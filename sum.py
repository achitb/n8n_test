def sum_three_ints(a, b, c):
  """Calculates the sum of three integers.

  Args:
    a: The first integer.
    b: The second integer.
    c: The third integer.

  Returns:
    The sum of the three integers.
  """
  return a + b + c

# Example usage:
num1 = 10
num2 = 20
num3 = 30
total = sum_three_ints(num1, num2, num3)
print(f"The sum of {num1}, {num2}, and {num3} is: {total}")